#!/usr/bin/env node
/**
 * Build LaTeX templates → public/lib/latex-templates.js bundle.
 *
 * Output is loaded at runtime via `loadScript('/lib/latex-templates.js')`
 * in src/hooks/useLatexEngine.ts, which Vite serves from public/. There
 * is no JS/TS code that imports this file directly.
 *
 * Why Node (not bash + sed): the previous shell version pipelined three
 * `sed` invocations to escape backslashes, backticks, and dollars in
 * the template literals. On macOS (BSD sed) this produced output X; on
 * Linux (GNU sed) it produced output X' where X != X' — different
 * escaping of the same chars. The bundle-sync CI check
 * (.github/workflows/test.yml) then false-failed on every PR because
 * the committed bundle (built locally on macOS) didn't match the
 * CI-regenerated bundle (built on Ubuntu). Node's string replace is
 * deterministic across platforms — same input always yields same
 * bytes.
 *
 * Output is also explicitly deterministic — no timestamp etc., so the
 * sync check uses a simple `git diff --exit-code`. Use
 * `git log public/lib/latex-templates.js` to see when it was last
 * regenerated.
 */
import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = fileURLToPath(new URL('.', import.meta.url));
const OUTPUT_PUBLIC = join(__dirname, 'public', 'lib', 'latex-templates.js');

/**
 * Escape a string for embedding inside a JS template literal (backtick-
 * delimited string). Three chars must be escaped:
 *   \  → \\   (must come first or it gets compounded)
 *   `  → \`
 *   $  → \$   (would otherwise start a ${...} interpolation)
 *
 * Order matters: backslash MUST be replaced first, otherwise the `\`
 * we add for backtick / dollar gets re-doubled.
 */
function escapeForTemplateLiteral(s) {
  return s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$');
}

const HEADER = `/**
 * LaTeX Templates for SwiftLaTeX Browser Compilation
 * Auto-generated by build-templates.mjs - DO NOT EDIT
 *
 * Output is deterministic — same tex/ input always produces the
 * same bundle. (No timestamp etc., so the bundle-sync CI check
 * in test.yml can use a simple \`git diff --exit-code\`. Use
 * \`git log public/lib/latex-templates.js\` to see when it was last
 * regenerated.)
 */

const LATEX_TEMPLATES = {
`;

const FOOTER = `};

// Export for use in app.js
if (typeof window !== 'undefined') {
  window.LATEX_TEMPLATES = LATEX_TEMPLATES;
}
`;

function emit(path, content) {
  // Each entry is `  'KEY': \`<escaped content>\`,` — ASCII-safe and
  // diffable. The leading + trailing newlines around the escaped
  // content match the bash-version's `echo` output exactly so a
  // first-time regeneration produces a content-identical result.
  return `  '${path}': \`\n${escapeForTemplateLiteral(content)}\`,\n`;
}

let body = '';

// 1. tex/main.tex (lives at the root of tex/)
const mainTex = readFileSync(join(__dirname, 'tex', 'main.tex'), 'utf-8');
body += emit('tex/main.tex', mainTex);

// 2. tex/templates/*.tex
body += '\n  // Template files\n';
const templatesDir = join(__dirname, 'tex', 'templates');
const files = readdirSync(templatesDir)
  .filter((name) => name.endsWith('.tex'))
  .sort(); // Stable order regardless of filesystem listing.

for (const name of files) {
  const content = readFileSync(join(templatesDir, name), 'utf-8');
  body += emit(`tex/templates/${name}`, content);
}

const output = HEADER + body + FOOTER;
writeFileSync(OUTPUT_PUBLIC, output);

// eslint-disable-next-line no-console
console.log(`Generated ${OUTPUT_PUBLIC} (${output.length.toLocaleString()} bytes, ${files.length + 1} templates)`);
